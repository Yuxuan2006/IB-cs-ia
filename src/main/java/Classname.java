    public void CreateClass() {
        // Retrieve input values from text fields
        String classname = jTextField2.getText();
        String level = jTextField1.getText();
        String course = jTextField4.getText();
        
        Class classobj = new Class();
        String id = UUID.randomUUID().toString();
        classobj.setClassId(id);
        classobj.setCourse(course);
        classobj.setLevel(level);
        classobj.setTeacherId(teacher.getId());
        classobj.setName(classname);
        
        teacher.setClass_id(id);
        teacher.setTeacher_id(teacher.getId());
        
        try {
            db.addClass(classobj); // Save teacher to the database
            db.addTeacher(teacher); // Save teacher to the database
            
            
            ImageIcon icon = new ImageIcon("Success.PNG");
            JOptionPane.showMessageDialog(
                    null,
                    "<html><b style=\"color: green; font-size: 10px\">Registration successful</b></html>",
                    "Success",
                    JOptionPane.INFORMATION_MESSAGE,
                    icon
            );
            
            this.setVisible(false); // Hide the registration form
            new Login().setVisible(true); // Show the login form

        } catch (HeadlessException | SQLException e) {
            e.printStackTrace();
        }
    }
    
